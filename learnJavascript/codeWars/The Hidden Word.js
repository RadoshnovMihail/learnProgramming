// Майя еженедельно пишет статьи в известный журнал, но каждый раз, когда она собирается отправить статью редактору, ей не хватает одного слова. Без этого слова статья будет неполной. У Майи есть друг, Дэн, и он очень хорошо разбирается в словах, но не любит просто так отдавать их. Он присылает Майе номер, и она должна найти скрытое слово. Слова могут содержать только буквы:

// "a", "b", "d", "e", "i", "l", "m", "n", "o" и "t".
// К счастью, у Maya есть ключ:

// "a" : 6
// "b" : 1 
// "d" : 7
// "e" : 4
// "i" : 3
// "l" : 2
// "m" : 9
// "n" : 8
// "o" : 0
